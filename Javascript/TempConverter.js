// Convert temperature from Celsius (°C) to Fahrenheit (°F) 
// Formula : Celsius = (Fahrenheit - 32) * 5/9
// Formula : Fahrenheit = (Celsius * 9/5) + 32


// Temperature is 25°C. Calcuate Temperature from Celsius (°C) to Fahrenheit (°F) 
Fahrenheit = (25 * 9/5) + 32;
console.log(Fahrenheit);    // Output : 77

// Temperature is 86°F. Calcuate Temperature from Fahrenheit (°F) to Celsius (°C) 
Celsius = (86 - 32) * 5/9
console.log(Celsius);       // Output : 30



